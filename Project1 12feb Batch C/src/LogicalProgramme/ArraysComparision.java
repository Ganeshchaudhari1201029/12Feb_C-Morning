package LogicalProgramme;

import java.util.Arrays;

public class ArraysComparision {
	
	public static void main(String[] args)
	{
		int [] ar= {20,30,40};
		
		int [] ar1= {30,40,50};
		
		int [] ar2= {20,30,40};
		
		//compare 2 arrays information
		System.out.println(Arrays.equals(ar,ar2));//false
		
		System.out.println(Arrays.equals(ar2,ar1));//true
		
		System.out.println(Arrays.equals(ar2,ar1));//false
		
	}
	}

	
	
	
	


