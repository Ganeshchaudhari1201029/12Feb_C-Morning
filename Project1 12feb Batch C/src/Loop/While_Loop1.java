package Loop;

public class While_Loop1 {
	
	//Print 1 to 5 no
	public static void main(String[] args) {
		
		int i=1; // Start Condition 
		
		while(i<=5) {   //End Condition
			System.out.println(i);// 1 2 3 4 5
			System.out.println("----");
			i++;  // Incriment/ Decrement Condition
			
		}
		
	}

}
