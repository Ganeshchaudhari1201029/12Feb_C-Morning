package Loop;

public class For_Loop4 {
	public static void main(String[] args) {
		
		//Print 50 To 1 No
		
		for (int i=50; i>=1;i--) {
			// i=5  5>=1  4
			// i=5   4>=1 3
			//       3>=1 2
			//       2>=1  1
			System.out.println(i); // 5 4 3 2 1 
			System.out.println("---");
		}
		
	}

}
