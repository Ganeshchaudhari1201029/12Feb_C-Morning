package Loop;

public class Do_WhileLoop1 {
	// print 1 to 5 no
	public static void main(String[] args) {
		
		int i=1;  //Starting Condition
		
		do {
		        i++;                           //Increment & Decrement
		System.out.println(i);
		System.out.println("----");
	
	}
		while(i<=5);                     //End Condition


}
}