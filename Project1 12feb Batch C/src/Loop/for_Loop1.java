package Loop;

public class for_Loop1 {
	
	//print 1 to 5 no.
	public static void main(String[] args)
	
	// i=1  1<=5    2
	//      2<=5    3
	//      3<=5    4
	//      4<=5    5
	{ for (int i=1;i<=5; i++) {
		// For Loop Body
		
		System.out.println("Sirial No="+i);  // 1 2 3 4 5
		System.out.println("-----------------------");
		
		}
	
		}
	}


