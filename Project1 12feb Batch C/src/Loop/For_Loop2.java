package Loop;

public class For_Loop2 {
	public static void main(String[] args) {
		// print 1 to 20 Odd No
		
		
		for (int i=1; i<=20; i=i+2 ) {
			
			System.out.println(i);
			System.out.println("--------------");
		}
		for (int i=10; i<=100; i=i+5) {
			
			System.out.println(i);
			System.out.println("---");
		}
		for (int i=200; i>=50; i=i-20) {
			System.out.println(i);
			System.out.println("---");
		}
		for (int i=20; i>=2; i=i-2) {
			System.out.println(i);
		}
	}

}
