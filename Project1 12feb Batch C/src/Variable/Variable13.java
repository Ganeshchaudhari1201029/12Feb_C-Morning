package Variable;

public class Variable13 {
		
		public static void main(String[]args) {
			// veriable declaration
			String StudentFullname;   // Data type Variable name
			
			int StudentRollNo;    // Data type variable name
			
			char StudentGrade;
			
			float Studentpercentage;
			
			// variable initialization
			
			StudentFullname=("Ganesh Sarjerao Chaudhari"); // variable name="variable information";
			
			StudentRollNo=(28); //Variable name=variable information;
			
			StudentGrade=('A'); //Variable name='Variable information';
			
			Studentpercentage=(71.13f); // Variable Name=Variable information name f;
			
			
			//Usage
			System.out.println("StudentName="+StudentFullname);
			System.out.println("StudentRollNO="+StudentRollNo);
			System.out.println("StudentGrade='A'"+StudentGrade);
			System.out.println("StudentPercentage="+Studentpercentage+"%");}
		
		
		
		
}


			
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	


